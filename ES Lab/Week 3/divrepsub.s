	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R1,=VAL1
	LDR R2,=VAL2
	LDR R3,[R1]
	LDR R4,[R2]
	MOV R5,#0
	LDR R6,=RESULT
UP	CMP R3,R4
	BCC STORE
	SUB R3,R4
	ADD R5,#1
	B UP
STORE
	STR R5,[R6],#4
	STR R3,[R6]
Stop
	B Stop
VAL1 DCD 76
VAL2 DCD 4
	AREA mydata, DATA, READWRITE
RESULT DCD 0,0
	END